public class Lunchera {
        
    public String material;
    public String marca;
    public String modelo;
    public double espacio;
    
    public Lunchera(){
        material = "Desconocido";
        marca = "";
        modelo = "Desconocido";   
        espacio = 0;
    }

    
     public Lunchera( String material,
                   String marca,
                   String modelo,
                   double espacio
                ){
        this.material = material;
        this.espacio = espacio;
        this.marca = marca;
        this.modelo = modelo;
      
        
    
    
}
    
  
     
}
